/// Attach the current sidecar token as `X-Sidecar-Token` to a request builder.
/// Returns the builder unchanged when no token is stored (e.g. during restart).
fn with_token(req: reqwest::blocking::RequestBuilder) -> reqwest::blocking::RequestBuilder {
    match SIDECAR_TOKEN.lock().ok().and_then(|g| g.clone()) {
        Some(t) => req.header("X-Sidecar-Token", t),
        None => req,
    }
}

/// Wait up to ~10 s for the sidecar HTTP server to respond on :9001.
fn wait_for_sidecar() -> bool {
    let client = reqwest::blocking::Client::builder()
        .timeout(Duration::from_secs(2))
        .build()
        .unwrap();

    for _ in 0..10 {
        if client.get("http://127.0.0.1:9001/health").send().is_ok() {
            return true;
        }
        thread::sleep(Duration::from_secs(1));
    }
    false
}

