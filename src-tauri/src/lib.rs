/// Post a log message to the sidecar's /log endpoint so it appears in the agent UI.
fn sidecar_log(client: &reqwest::blocking::Client, level: &str, msg: &str) {
    let body = format!(r#"{{"level":"{}","msg":"{}"}}"#, level, msg.replace('"', "\\\""));
    let _ = with_token(
        client
            .post("http://127.0.0.1:9001/log")
            .header("Content-Type", "application/json")
            .body(body),
    )
    .send();
}

/// One-shot sidecar log — creates its own client, use when no client is available.
fn sidecar_log_quick(level: &str, msg: &str) {
    if let Ok(client) = reqwest::blocking::Client::builder()
        .timeout(Duration::from_secs(3))
        .build()
    {
        sidecar_log(&client, level, msg);
    }
}

/// Check GitHub releases API for a newer version.
/// Returns Ok(Some(tag)) if an update is available, Ok(None) if already on latest,
/// or Err(description) if the API call failed (network error, auth, 404, etc.).
fn check_for_update(current_version: &str) -> Result<Option<String>, String> {
    let client = reqwest::blocking::Client::builder()
        .timeout(Duration::from_secs(10))
        .build()
        .map_err(|e| format!("Failed to build HTTP client: {e}"))?;

    let mut req = client
        .get("https://api.github.com/repos/Bishi/auto-scraper-agent/releases/latest")
        .header("User-Agent", "auto-scraper-agent");

    // Optional GitHub token — required when the repo is private.
    // Set GITHUB_TOKEN at build time (e.g. in the GitHub Actions release workflow).
    if let Some(token) = option_env!("GITHUB_TOKEN") {
        req = req.header("Authorization", format!("Bearer {token}"));
    }

    let response = req.send().map_err(|e| format!("Network error: {e}"))?;
    let status = response.status();
    if !status.is_success() {
        return Err(format!("GitHub API returned {status}"));
    }

    let release: GitHubRelease = response
        .json()
        .map_err(|e| format!("Failed to parse GitHub response: {e}"))?;

    let latest = release.tag_name.trim_start_matches('v');
    if latest != current_version {
        Ok(Some(release.tag_name))
    } else {
        Ok(None)
    }
}

