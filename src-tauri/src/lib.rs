// Prevents two concurrent update flows (startup check + manual "Check for Updates" click).
static UPDATE_IN_PROGRESS: AtomicBool = AtomicBool::new(false);
// Latest available version tag found by background check, shown non-intrusively in the UI.
static AVAILABLE_UPDATE: Mutex<Option<String>> = Mutex::new(None);
// Set to true once a manual update check triggered by the user completes (either found or not found).
// Lets the renderer stop polling immediately instead of waiting the full 20s timeout.
static UPDATE_CHECK_DONE: AtomicBool = AtomicBool::new(false);
// Last error from a user-initiated update check, shown in the Settings UI.
static UPDATE_CHECK_ERROR: Mutex<Option<String>> = Mutex::new(None);
// Download progress string shown in the agent UI, e.g. "45 / 83 MB (54%)". None when idle.
static DOWNLOAD_PROGRESS: Mutex<Option<String>> = Mutex::new(None);
// Ephemeral shared secret generated by the sidecar on each startup.
// Captured from sidecar stdout by the watchdog and required on every HTTP request
// to the sidecar (except OPTIONS preflights and the /health endpoint).
static SIDECAR_TOKEN: Mutex<Option<String>> = Mutex::new(None);

