/// Triggers the download + install flow. Re-checks GitHub at click time to always
/// get the absolute latest release (not the cached badge version).
/// Called from the renderer when the user clicks the update badge.
#[tauri::command]
fn install_update(app: AppHandle) {
    // Guard against concurrent clicks — but reset before calling
    // handle_update_available so it can take ownership of the flag itself.
    if UPDATE_IN_PROGRESS.swap(true, Ordering::SeqCst) {
        return; // already in progress
    }
    UPDATE_IN_PROGRESS.store(false, Ordering::SeqCst);
    UPDATE_CHECK_DONE.store(false, Ordering::SeqCst);

    let current_version = app.package_info().version.to_string();
    thread::spawn(move || {
        sidecar_log_quick("info", "[agent] Update install initiated by user");
        match check_for_update(&current_version) {
            Ok(Some(latest_tag)) => {
                if let Ok(mut guard) = AVAILABLE_UPDATE.lock() {
                    *guard = Some(latest_tag.clone());
                }
                if let Ok(mut guard) = UPDATE_CHECK_ERROR.lock() { *guard = None; }
                UPDATE_CHECK_DONE.store(true, Ordering::SeqCst);
                handle_update_available(&app, &latest_tag);
            }
            Ok(None) => {
                // Already on latest — clear badge and any previous error
                if let Ok(mut guard) = AVAILABLE_UPDATE.lock() { *guard = None; }
                if let Ok(mut guard) = UPDATE_CHECK_ERROR.lock() { *guard = None; }
                UPDATE_CHECK_DONE.store(true, Ordering::SeqCst);
                sidecar_log_quick("info", "[agent] Update check: already on latest version");
            }
            Err(e) => {
                if let Ok(mut guard) = UPDATE_CHECK_ERROR.lock() { *guard = Some(e.clone()); }
                UPDATE_CHECK_DONE.store(true, Ordering::SeqCst);
                sidecar_log_quick("error", &format!("[agent] Update check failed: {e}"));
            }
        }
    });
}

/// Returns true once the manual update check triggered by install_update has completed.
/// Lets the renderer stop polling immediately instead of waiting the full 20s timeout.
#[tauri::command]
fn is_update_check_done() -> bool {
    UPDATE_CHECK_DONE.load(Ordering::SeqCst)
}

/// Returns the error message from the last user-initiated update check, or null if no error.
#[tauri::command]
fn get_update_check_error() -> Option<String> {
    UPDATE_CHECK_ERROR.lock().ok().and_then(|g| g.clone())
}

/// Returns the current download progress string (e.g. "45 / 83 MB (54%)"),
/// or null when no download is in progress.
#[tauri::command]
fn get_download_progress() -> Option<String> {
    DOWNLOAD_PROGRESS.lock().ok().and_then(|g| g.clone())
}

